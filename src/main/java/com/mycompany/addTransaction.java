    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String customer = (String) customerList.getSelectedItem();
        String car = (String) carList.getSelectedItem();
        Date sd = startDateChooser.getDate();
        Date ed = endDateChooser.getDate();
        String cost = costfield.getText();
        RentCar rc = new RentCar(car);

        if (rc.checkCarAvailability(sd)) {
            long diffInMillies = Math.abs(ed.getTime() - sd.getTime());
            long diff = TimeUnit.DAYS.convert(diffInMillies, TimeUnit.MILLISECONDS) + 1;
            rc.addTransaction(sd, ed, car, customer, diff, cost);
            JOptionPane.showMessageDialog(null, "Transaction added successfully! ");
            new mainMenu().setVisible(true);
            dispose();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

