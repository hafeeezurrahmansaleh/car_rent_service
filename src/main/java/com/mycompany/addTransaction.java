    public void getCost() {
        double totalCost = 0;
        try {
            String car = (String) carList.getSelectedItem();
            Date sd = startDateChooser.getDate();
            Date ed = endDateChooser.getDate();
            car ca = new car();
            long diffInMillies = Math.abs(ed.getTime() - sd.getTime());
            long diff = TimeUnit.DAYS.convert(diffInMillies, TimeUnit.MILLISECONDS) + 1;
            String[] carInfo = ca.getCarInfo(car);
            double rentPerDay = Double.parseDouble(carInfo[2]);
            totalCost = diff*rentPerDay;
            costfield.setText(""+totalCost);
            
        }
        catch(Exception e){
                System.out.println(e);
                costfield.setText("0");
        }
        
    }

